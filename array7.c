#include<stdio.h>
int main(){
    int a[100],n,i,temp=0;
    printf("enter a element");
    scanf("%d",&n);
    for(i=0;i<n;i++){
        scanf("%d",&a[i]);
    }
    for(i=0;i<n/2;i++){ 
        temp=a[i];
        a[i]=a[n-i-1];
        a[n-i-1]=temp;
    }
    printf("reverse array is :");
    for(i=0;i<n;i++){
        printf("%d",a[i]);
    }
    return 0;

}
